async def get_referral_stats(referrer_code: str):
    async with aiosqlite.connect("users.db") as conn:
        # üîΩ –¢–µ–ø–µ—Ä—å –±–µ—Ä—ë–º clicks –∏–∑ —Ç–∞–±–ª–∏—Ü—ã users (–ø–æ referral_code)
        cursor = await conn.execute(
            "SELECT clicks, sum_my FROM users WHERE referral_code = ?",
            (referrer_code,)
        )
        row = await cursor.fetchone()
        total_clicks = row[0] if row else 0
        # sum_my –∑–¥–µ—Å—å ‚Äî —ç—Ç–æ, –≤–æ–∑–º–æ–∂–Ω–æ, –∑–∞—Ä–∞–±–æ—Ç–æ–∫ –≤–ª–∞–¥–µ–ª—å—Ü–∞, –Ω–æ –µ—Å–ª–∏ –Ω—É–∂–Ω–∞ —Å—É–º–º–∞ –ø–æ–∫—É–ø–æ–∫ —Ä–µ—Ñ–µ—Ä–∞–ª–æ–≤ ‚Äî —Å–º. –Ω–∏–∂–µ

        # –ü–æ–ª—É—á–∞–µ–º —Å—É–º–º—É –ø–æ–∫—É–ø–æ–∫ —Ä–µ—Ñ–µ—Ä–∞–ª–æ–≤ –∏–∑ —Ç–∞–±–ª–∏—Ü—ã referals (–µ—Å–ª–∏ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è)
        cursor = await conn.execute(
            "SELECT amount FROM referals WHERE code = ?",
            (referrer_code,)
        )
        row = await cursor.fetchone()
        total_amount = row[0] if row else 0

        # –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –ø–æ –ø–æ–¥–ø–∏—Å–∫–∞–º —Ä–µ—Ñ–µ—Ä–∞–ª–æ–≤
        cursor = await conn.execute("""
            SELECT 
                SUM(CASE WHEN has_trial = 1 AND (sum_my = 0 OR sum_my IS NULL) THEN 1 ELSE 0 END),
                SUM(CASE WHEN sum_my > 0 THEN 1 ELSE 0 END),
                SUM(CASE WHEN has_trial = 0 AND (sum_my = 0 OR sum_my IS NULL) THEN 1 ELSE 0 END)
            FROM users
            WHERE referrer_code = ?
        """, (referrer_code,))
        
        stats = await cursor.fetchone()
        free_subs = stats[0] or 0
        paid_subs = stats[1] or 0
        no_subs = stats[2] or 0

    return {
        "total_amount": total_amount,
        "total_clicks": total_clicks,  # ‚úÖ –¢–µ–ø–µ—Ä—å –∏–∑ —Ç–∞–±–ª–∏—Ü—ã users
        "free_subs": free_subs,
        "paid_subs": paid_subs,
        "no_subs": no_subs
    }